#include "libft.h"

char	*ft_strdup(const char *src)
{
	size_t	len_str;
	char	*dest;
	int		i;

	i = 0;
	len_str = 0;
	while (src[len_str])
		len_str++;
	dest = (char *)malloc((len_str + 1) * sizeof(char));
	if (!dest)
		return (0);
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
